package Pratice;

public class Variable {

	public static void main(String[] args) {
		int a=12;//a is variable & int is datatype 
		int b=10;
		
		//Arthematic operators
		System.out.println("Sum="+(a+b));//Operators
		System.out.println("subtarct="+(a-b));//Logical
		System.out.println("multiply="+(a*b));
		System.out.println("Divede="+(b/2));
		System.out.println("Modulo="+(b%3));//Remider
		
		//Relatinal Operators Or Comparisons Operators
		
		int c=30;
		int d=20;
	
		System.out.println("Equal="+(c==d));
		System.out.println("Greater than="+(c>d));
		System.out.println("Less Than="+(c<d));
		System.out.println("Greater Or Equal="+(c>=d));
		System.out.println("Less Or equal="+(c<=d));
		System.out.println("Not Equals="+(c!=d));
		
		//Logical Operators
		boolean e=true;
		boolean f=false;
		System.out.println("and Gate="+(e&&f));
		System.out.println("Or Gate="+(e||f));
		System.out.println("Not="+(!e));
		System.out.println("Not="+(!f));
		
		//Increment And Decrement
		a++;
		//int t=a+1;
		a--;
		a--;
	int y=a-1;
		System.out.println(y);
		
		///
		
		
		
				

	}

}
