package IMPProgramInterview;

public class FactorialNumber {
	
	//if no is 5 the is factorial no is 1*2*3*4*5=factorial no
	
	
	public static void main(String[] args) {
		
		int n=6;
		int fact=1;
		
		for(int i=1;i<=n;i++) {
			fact=fact*i;
			//System.out.println(fact); if want step by step output
		}
		
		System.out.println(fact);//if want final output
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
