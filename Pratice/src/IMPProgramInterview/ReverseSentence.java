public class ReverseSentence {
	public static void main(String[] args) {
		
		String s="I Love India";
		String r="";
		
		String ar[]=s.split(" ");
		
		for(int i=ar.length-1;i>=0;i--) {
			
			r=r+ar[i]+" ";
		}
		
		System.out.println("rever===>"+r+" ");
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
