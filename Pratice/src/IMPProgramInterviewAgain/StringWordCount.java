package IMPProgramInterviewAgain;

public class StringWordCount {
	public static void main(String[]args) {
		
		
		
		String s="we belong to m group";
		char[] c=s.toCharArray();
		int count=0;
		for(char ch:c) {
	
			if(ch=='c') {
				count++;
			}
			
		}
		
		System.out.println(count+1);
		
		
		
		
		
		
		
		
		
		
		
	}

}
